% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
axes(handles.axes1);
n = get(handles.listbox2, 'value');
f = get(handles.listbox3, 'value');
iso = get(handles.listbox4, 'value');
path = '.\Moon\';
switch n
    case 1
        path = [path '1_'];
    case 2
        path = [path '15_'];
    case 3
        path = [path '125_'];
end
switch iso
    case 1
        path = [path '100_'];
    case 2
        path = [path '800_'];
    case 3
        path = [path '3200_'];
end
switch f
    case 1
        path = [path '8.jpg'];
    case 2
        path = [path '16.jpg'];
    case 3
        path = [path '32.jpg'];
end
image = imread(path);
imshow(image);

